"""
LLM-as-judge evaluation metrics for RAG pipelines.

RAG Pipeline Position:
  Document → Chunks → Embeddings → Vector Store → Retrieval → Generator → Answer
                                                                              |
                                                                         [EVALUATOR]
                                                                              ^^^
  This module sits AFTER the generator. It receives the final answer plus the
  contexts that were retrieved and uses an LLM as an automated judge to score:
    - Faithfulness: does the answer stay true to the retrieved context?
    - Answer Relevancy: does the answer actually address the question asked?
    - Context Precision: were the retrieved chunks actually useful?

Design decisions:
  - LLM-as-judge (RAGAS-inspired) over heuristics because factual grounding
    is a semantic judgment that keyword matching cannot reliably make.
  - Strict JSON output format so scores are machine-readable and storable in
    the MessageEvaluation table without further parsing.
  - `llm` is passed in (dependency injection) so callers can supply any
    LLMHandler instance — or a mock in tests — without coupling this module
    to a specific model.
"""

from __future__ import annotations

import json
import logging
import re

logger = logging.getLogger(__name__)


# --------------------------------------------------------------------------- #
# Internal helpers                                                             #
# --------------------------------------------------------------------------- #


def _parse_json_response(text: str) -> dict | None:
    """Strip markdown code fences and parse the JSON payload inside.

    WHY: LLMs often wrap JSON in ```json ... ``` fences even when instructed
    not to. Stripping fences before parsing is more robust than demanding the
    model produce bare JSON — models are inconsistent about following that
    instruction perfectly.

    Args:
        text: Raw string returned by the LLM.

    Returns:
        Parsed dict, or None if the string cannot be decoded as JSON.
    """
    # PATTERN: Strip markdown fences unconditionally, then parse. Stripping
    #          fences from bare JSON is harmless (no backticks to remove), so
    #          a single code path handles both fenced and unfenced responses.
    stripped = text.strip()

    # Remove ```json ... ``` or ``` ... ``` fences
    stripped = re.sub(r"^```(?:json)?\s*", "", stripped)
    stripped = re.sub(r"\s*```$", "", stripped)
    stripped = stripped.strip()

    try:
        return json.loads(stripped)
    except (json.JSONDecodeError, ValueError):
        logger.warning("Failed to parse LLM JSON response: %r", text[:200])
        return None


def _clamp_score(value: float) -> float:
    """Clamp a score to the [0.0, 1.0] range.

    WHY: LLMs are not guaranteed to return scores within bounds — a model
    might output 1.5 or -0.3 despite the prompt requesting 0.0–1.0. Clamping
    prevents out-of-range values from producing nonsensical UI displays (e.g.
    "150%") and violating the normalized score contract.
    """
    return max(0.0, min(1.0, value))



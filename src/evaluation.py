# Evaluation functions                                                         #
# --------------------------------------------------------------------------- #


def evaluate_faithfulness(
    answer: str,
    contexts: list[str],
    llm,
) -> tuple[float, str, str | None]:
    """Evaluate whether the answer is grounded in the retrieved contexts.

    This is the FAITHFULNESS metric from RAGAS. The judge LLM decomposes the
    answer into atomic claims and checks each claim against the context.
    A claim is "supported" if and only if the context explicitly contains
    or implies it — the judge must not hallucinate support.

    TRADE-OFF: Asking the LLM to enumerate individual claims is more
    informative than a single yes/no but costs more tokens. The per-claim
    breakdown also makes evaluation results auditable and debuggable.

    Args:
        answer: The answer generated by the RAG pipeline.
        contexts: List of retrieved text chunks used to generate the answer.
        llm: An LLMHandler instance (or compatible mock) with a
             `generate(prompt, system_prompt=)` method.

    Returns:
        Tuple of:
          - score (float): Fraction of claims supported by context, 0.0–1.0.
          - reasoning (str): Human-readable explanation from the judge.
          - details (str | None): Raw JSON string with per-claim breakdown,
            or None if the LLM returned malformed output.
    """
    # WHY system prompt: separating the task description from the data makes
    #     the judge focus its reasoning on evaluation, not on answering the
    #     original question. This is a standard LLM-as-judge technique.
    system_prompt = (
        "You are a strict faithfulness evaluator for a RAG system. "
        "Your task is to decompose an answer into its TOP 5 most important "
        "atomic claims and verify each claim against the provided context passages. "
        "A claim is supported ONLY if the context explicitly states or directly implies it. "
        "Do not assume knowledge outside the context. "
        "Keep your response concise — at most 5 claims. "
        "Respond ONLY with a valid JSON object — no prose, no code fences."
    )

    context_block = "\n\n".join(
        f"[Context {i+1}]:\n{ctx}" for i, ctx in enumerate(contexts)
    )

    # WHY double braces: we're inside an f-string but need literal { } in the
    #     JSON schema example so the model knows the exact output shape.
    user_prompt = (
        f"Answer to evaluate:\n{answer}\n\n"
        f"Retrieved context:\n{context_block}\n\n"
        "Return a JSON object with this exact schema:\n"
        "{{\n"
        '  "claims": [\n'
        '    {{"claim": "<claim text>", "supported": true|false, "evidence": "<quote or null>"}}\n'
        "  ],\n"
        '  "score": <float 0.0-1.0>,\n'
        '  "reasoning": "<one sentence summary>"\n'
        "}}"
    )

    raw = llm.generate(user_prompt, system_prompt=system_prompt)
    parsed = _parse_json_response(raw)

    if parsed is None:
        # PATTERN: Fail-safe return — a parse failure means we cannot trust
        #          any score, so 0.0 is the conservative default rather than
        #          silently propagating garbage data into the database.
        return (
            0.0,
            "Evaluation failed: LLM returned malformed JSON.",
            None,
        )

    score = _clamp_score(float(parsed.get("score", 0.0)))
    reasoning = str(parsed.get("reasoning", ""))
    # Store the full parsed response as a JSON string for the details column
    details = json.dumps(parsed)

    return score, reasoning, details


def evaluate_answer_relevancy(
    question: str,
    answer: str,
    llm,
) -> tuple[float, str]:
    """Evaluate how directly and completely the answer addresses the question.

    This is the ANSWER RELEVANCY metric from RAGAS. Unlike faithfulness (which
    checks grounding), this metric checks alignment: does the answer actually
    respond to what was asked, even if it's factually correct?

    TRADE-OFF: We do NOT pass the contexts here intentionally — answer relevancy
    is purely a question-answer alignment judgment. Including context would blur
    the metric with faithfulness and make it harder to diagnose specific failure
    modes (e.g., answer is grounded but off-topic).

    Args:
        question: The original user question.
        answer: The answer generated by the RAG pipeline.
        llm: An LLMHandler instance (or compatible mock).

    Returns:
        Tuple of:
          - score (float): Relevancy score 0.0–1.0 (1.0 = directly addresses question).
          - reasoning (str): Judge's explanation.
    """
    system_prompt = (
        "You are an answer relevancy evaluator for a RAG system. "
        "Your task is to judge how directly and completely the given answer "
        "addresses the given question. "
        "Score 1.0 if the answer fully and directly addresses the question. "
        "Score 0.0 if the answer is completely off-topic or refuses to answer. "
        "Respond ONLY with a valid JSON object — no prose, no code fences."
    )

    user_prompt = (
        f"Question: {question}\n\n"
        f"Answer: {answer}\n\n"
        "Return a JSON object with this exact schema:\n"
        "{{\n"
        '  "score": <float 0.0-1.0>,\n'
        '  "reasoning": "<one sentence summary>"\n'
        "}}"
    )

    raw = llm.generate(user_prompt, system_prompt=system_prompt)
    parsed = _parse_json_response(raw)

    if parsed is None:
        return (
            0.0,
            "Evaluation failed: LLM returned malformed JSON.",
        )

    score = _clamp_score(float(parsed.get("score", 0.0)))
    reasoning = str(parsed.get("reasoning", ""))

    return score, reasoning


def evaluate_context_precision(
    question: str,
    contexts: list[str],
    llm,
) -> tuple[float, str, str | None]:
    """Evaluate how precise the retrieved context is relative to the question.

    This is the CONTEXT PRECISION metric from RAGAS. It asks whether the
    retrieved chunks actually contain information needed to answer the question,
    or whether the retriever returned noisy/irrelevant chunks alongside useful ones.

    A low score here signals a retrieval problem: the vector search is returning
    chunks that match superficially but don't help answer the question. This is
    distinct from faithfulness (generator problem) and relevancy (generation
    problem) — each metric isolates a different pipeline stage.

    WHY per-chunk judgment: knowing WHICH chunks were irrelevant tells you whether
    to tune embedding thresholds, reduce top-k, or improve chunking strategy.
    A single aggregate number hides actionable signal.

    Args:
        question: The original user question.
        contexts: List of retrieved text chunks (ordered by retrieval rank).
        llm: An LLMHandler instance (or compatible mock).

    Returns:
        Tuple of:
          - score (float): Fraction of retrieved chunks that are relevant, 0.0–1.0.
          - reasoning (str): Judge's explanation.
          - details (str | None): JSON string with per-chunk relevance judgments,
            or None if the LLM returned malformed output.
    """
    system_prompt = (
        "You are a context precision evaluator for a RAG system. "
        "Your task is to judge whether each retrieved text chunk contains "
        "information that is relevant to answering the given question. "
        "A chunk is relevant if it would help answer the question, even partially. "
        "Respond ONLY with a valid JSON object — no prose, no code fences."
    )

    chunks_block = "\n\n".join(
        f"[Chunk {i}]:\n{ctx}" for i, ctx in enumerate(contexts)
    )

    user_prompt = (
        f"Question: {question}\n\n"
        f"Retrieved chunks:\n{chunks_block}\n\n"
        "Return a JSON object with this exact schema:\n"
        "{{\n"
        '  "chunks": [\n'
        '    {{"chunk_index": <int>, "relevant": true|false}}\n'
        "  ],\n"
        '  "score": <float 0.0-1.0>,\n'
        '  "reasoning": "<one sentence summary>"\n'
        "}}"
    )

    raw = llm.generate(user_prompt, system_prompt=system_prompt)
    parsed = _parse_json_response(raw)

    if parsed is None:
        return (
            0.0,
            "Evaluation failed: LLM returned malformed JSON.",
            None,
        )

    score = _clamp_score(float(parsed.get("score", 0.0)))
    reasoning = str(parsed.get("reasoning", ""))
    details = json.dumps(parsed)

    return score, reasoning, details
